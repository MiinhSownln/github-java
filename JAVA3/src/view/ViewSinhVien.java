    void fillTable(List<SinhViens> list) {
        model = (DefaultTableModel) tbSinhVien.getModel();
        model.setRowCount(0);// xóa bảng       
        for (SinhViens sv : list) {
            model.addRow(sv.toDataRow());
        }
    }

    void showData(int index) {
        //lay sv tư list vi tri index
        SinhViens sv = service.getAll().get(index);
        txtId.setText(String.valueOf(sv.getId()));
        txtName.setText(sv.getName());
        //nếu combo: getSelectedIyem
        //nếu radio?/ if else
    }

    SinhViens readForm() {
        int id = Integer.parseInt(txtId.getText().trim());
        String name = txtName.getText();
        return new SinhViens(id, name);
    }

    void resetForm() {
        txtId.setText("");
        txtName.setText("");
    }

