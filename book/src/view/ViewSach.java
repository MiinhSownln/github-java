    void showData(int index) {
        Sach sv = service.getAll().get(index);
        txtMa.setText(sv.getMaSach());
        txtTen.setText(sv.getTenSach());
        cboThe.setSelectedItem(sv.getTheLoai());
        txtDon.setText(String.valueOf(sv.getDonGia()));
    }

    void fillTable(List<Sach> list) {
        model = (DefaultTableModel) tbSach.getModel();
        model.setRowCount(0);
        for (Sach book : list) {
            model.addRow(book.toDataRow());
        }
    }

    Sach readForm() {
        String id = txtMa.getText();
        String name = txtTen.getText();
        String the = (String) cboThe.getSelectedItem();
        double price = Double.parseDouble(txtDon.getText());
        return new Sach(id, name, the, price);
    }

    boolean check() {
        if (txtMa.getText().isEmpty() || txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "vui logn nhap");
            return false;
        }
        if (txtDon.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "vui logn nhap");
            return false;
        } else {
            try {
                if (Double.parseDouble(txtDon.getText()) < 0) {
                    JOptionPane.showMessageDialog(this, "0 ohu hop");
                    return false;

                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "sai dwu lieuy");
                return false;

            }
        }
        return true;
    }

