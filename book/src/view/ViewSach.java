    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (check()) {
            Sach book = readForm();
            if (service.getSach(book.getMaSach()) != null) {
                JOptionPane.showMessageDialog(this, "trungma");
            } else {
                if (service.addSinhVien(book) > 0) {
                    fillTable(service.getAll());
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (check()) {
            Sach book = readForm();
            index = tbSach.getSelectedRow();
            int id = Integer.parseInt(tbSach.getValueAt(index, 0).toString());
            if (service.update(book, id) > 0) {
                JOptionPane.showMessageDialog(this, "Succes");
                fillTable(service.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "No Succes");

            }

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String tl = (String) cboThe.getSelectedItem();
        if (service.find(tl).size() > 0) {
            JOptionPane.showMessageDialog(this, "Tim dc");
            fillTable(service.find(tl));
        } else {
            JOptionPane.showMessageDialog(this, " 0 Tim dc");
            model = (DefaultTableModel) tbSach.getModel();
            model.setRowCount(0);
        }
    }//GEN-LAST:event_btnFindActionPerformed

