    public Grade readForm() {
        Grade gr = new Grade();
        gr.setId(0);
        SinhVien sv = new SinhVien(txtMa.getText(), txtHoten.getText());
        gr.setMaSV(sv);
        gr.setTiengAnh(Double.parseDouble(Ta.getText()));
        gr.setTinHoc(Double.parseDouble(Th.getText()));
        gr.setGdtc(Double.parseDouble(Gdtc.getText()));
        
        return gr;
    }
    
    public void showData(int index) {
        Grade g = dGr.getAll().get(index);
        Ta.setText(String.valueOf(g.getTiengAnh()));
        Th.setText(String.valueOf(g.getTinHoc()));
        Gdtc.setText(String.valueOf(g.getGdtc()));
        txtMa.setText(g.getMaSV().getMaSV());
        txtHoten.setText(g.getMaSV().getHoTen());
        avg.setText(String.valueOf(g.getdiemTb()));
    }
