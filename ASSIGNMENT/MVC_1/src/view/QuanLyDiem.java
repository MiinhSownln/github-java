    public boolean check() {
        if (Ta.getText().isEmpty() || Th.getText().isEmpty() || Gdtc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui long nhap day du diem");
            return false;
        } else {
            try {
                if (Double.parseDouble(Ta.getText()) < 0 || Double.parseDouble(Ta.getText()) > 10) {
                    JOptionPane.showMessageDialog(this, "Du lieu k hop le");
                    return false;
                    
                }
                if (Double.parseDouble(Th.getText()) < 0 || Double.parseDouble(Th.getText()) > 10) {
                    JOptionPane.showMessageDialog(this, "Du lieu k hop le");
                    return false;
                    
                }
                if (Double.parseDouble(Gdtc.getText()) < 0 || Double.parseDouble(Gdtc.getText()) > 10) {
                    JOptionPane.showMessageDialog(this, "Du lieu k hop le");
                    return false;
                    
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Loi du lieu");
            }
        }
        return true;
    }
