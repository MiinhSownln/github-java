package model;

import java.util.Date;

/**
 *
 * @author 24dom
 */
public class KhoaHoc {

    private int maKH;
    private String maCD;
    private double hocPhi;
    private int thoiLuong;
    private Date ngayKH;
    private String ghiChu, maNV;
    private Date ngayTao;

    public KhoaHoc() {
    }

    public KhoaHoc(int maKH, String maCD, double hocPhi, int thoiLuong, Date ngayKH, String ghiChu, String maNV, Date ngayTao) {
        this.maKH = maKH;
        this.maCD = maCD;
        this.hocPhi = hocPhi;
        this.thoiLuong = thoiLuong;
        this.ngayKH = ngayKH;
        this.ghiChu = ghiChu;
        this.maNV = maNV;
        this.ngayTao = ngayTao;
    }

    public int getMaKH() {
        return maKH;
    }

    public void setMaKH(int maKH) {
        this.maKH = maKH;
    }

    public String getMaCD() {
        return maCD;
    }

    public void setMaCD(String maCD) {
        this.maCD = maCD;
    }

    public double getHocPhi() {
        return hocPhi;
    }

    public void setHocPhi(double hocPhi) {
        this.hocPhi = hocPhi;
    }

    public int getThoiLuong() {
        return thoiLuong;
    }

    public void setThoiLuong(int thoiLuong) {
        this.thoiLuong = thoiLuong;
    }

    public Date getNgayKH() {
        return ngayKH;
    }

    public void setNgayKH(Date ngayKH) {
        this.ngayKH = ngayKH;
    }

    public String getGhiChu() {
        return ghiChu;
    }

    public void setGhiChu(String ghiChu) {
        this.ghiChu = ghiChu;
    }

    public String getMaNV() {
        return maNV;
    }

    public void setMaNV(String maNV) {
        this.maNV = maNV;
    }

    public Date getNgayTao() {
        return ngayTao;
    }

    public void setNgayTao(Date ngayTao) {
        this.ngayTao = ngayTao;
    }

    @Override
    public String toString() {
        return this.maCD + "(" + this.ngayKH + ")";
    }

}
