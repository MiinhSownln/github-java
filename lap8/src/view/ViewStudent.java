    public void fillTable(List<Student> list) {
        model = (DefaultTableModel) tb.getModel();
        model.setRowCount(0);
        for (Student st : list) {
            model.addRow(st.toDataRow());
        }
    }

    boolean test() {
        if (txtId.getText().isEmpty() || txtAddress.getText().isEmpty() || txtPhone.getText().isEmpty() || txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui long nhap day du");
            return false;
        }
        if (!txtEmail.getText().contains("@")) {
            JOptionPane.showMessageDialog(this, "SAi dinh dáº¡ng");
            return false;
        }

        return true;
    }

    public void showData(int index) {
        Student st = service.getAll().get(index);
        txtId.setText(st.getId());
        if (st.isGender()) {
            rbNam.setSelected(true);
        } else {
            rbNu.setSelected(true);
        }
        txtAddress.setText(st.getAddress());
        txtPhone.setText(st.getPhone());
        txtEmail.setText(st.getEmail());
    }

    Student readForm() {
        String id = txtId.getText();
        boolean gender = false;
        if (rbNam.isSelected()) {
            gender = true;
        }
        String diachi = txtAddress.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        return new Student(id, gender, diachi, phone, email);
    }

