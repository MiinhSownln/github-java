    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int confirm = JOptionPane.showConfirmDialog(this, "bn muon thoat k");
        if (confirm == 0) {
            if (txtId.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "vui long nhap ma de xoa");
            } else {
                if (service.getStudent(txtId.getText()) != null) {
                    int index = tb.getSelectedRow();
                    String id = tb.getValueAt(index, 0).toString();
                    if (service.delete(id) > 0) {
                        fillTable(service.getAll());
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "ma k ton tai");
                }
            }

        } else {

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (test()) {
            Student st = this.readForm();
            if (service.getStudent(st.getId()) != null) {
                JOptionPane.showMessageDialog(this, "Trung ma");
            } else {
                if (service.add(st) > 0) {
                    JOptionPane.showMessageDialog(this, "success");
                    fillTable(service.getAll());
                } else {
                    JOptionPane.showMessageDialog(this, " no success");
                }
            }
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

